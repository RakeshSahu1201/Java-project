	private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jcSubjectActionPerformed
		
		subjectName = jcSubject.getSelectedItem().toString();
		
		if(subjectName.isEmpty()) {
			System.out.println("please select the subject.");
			return;
		}
		List<Integer> examIds = ExamDao.getExamIdBySubject(subjectName);
		jcExamId.removeAllItems();
		if (examIds.isEmpty()) {
			System.out.println("no exam has been setted of " + subjectName);
			return;
		}
		
		examIds.stream().forEach(id -> jcExamId.addItem(String.valueOf(id)));

	}// GEN-LAST:event_jcSubjectActionPerformed

	private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEditActionPerformed

		if (jcExamId.getSelectedItem() == null) {
			System.out.println("please select exam id");
			return;
		}

		String examId = jcExamId.getSelectedItem().toString();
		showFrame = new EditQuestionsFrame(Integer.parseInt(examId),subjectName);
		showFrame.setVisible(true);
		this.dispose();

	}// GEN-LAST:event_btnEditActionPerformed

	private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBackActionPerformed
		showFrame = new AdminOptionFrame();
		showFrame.setVisible(true);
		this.dispose();
	}// GEN-LAST:event_btnBackActionPerformed

