	private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jcSubjectActionPerformed

		jcExamId.removeAllItems();

		if (jcSubject.getSelectedItem().toString().isEmpty())
			return;

		subject = jcSubject.getSelectedItem().toString();
		Function<Exam, Integer> idExtractor = exam -> exam.getExamID();

		List<Integer> examIds = ExamDao.getExamIdBySubject(subject);
		List<Integer> performerIds = PerformanceDao.getPerformerExamByUserId(UserProfile.getUserId()).stream()
				.map(idExtractor).collect(Collectors.toList());
		Predicate<Integer> check = id -> performerIds.contains(id);

		List<Integer> nonPerformerExamIds = examIds.stream().filter(check.negate()).collect(Collectors.toList());

		System.out.println("All the exam id of the subject c");
		System.out.println(examIds);

		System.out.println("___________________________________________________________");
		System.out.println("exam id of which exam the student has appreared");
		System.out.println(performerIds);
		System.out.println("___________________________________________________________");
		System.out.println("exam id for which the student can apprear");
		System.out.println(nonPerformerExamIds);

		nonPerformerExamIds.forEach(id -> jcExamId.addItem(String.valueOf(id)));

	}// GEN-LAST:event_jcSubjectActionPerformed

