    private void btnDeleteEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEmpActionPerformed
        try
        {
            int empid = Integer.parseInt(txtEmpno.getText().trim());
            Boolean result = EmployeeDao.deleteEmployeeById(empid);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Employee Deleted successfully!","Message!",JOptionPane.INFORMATION_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(null,"Employee not found with given id!","Not Found!",JOptionPane.INFORMATION_MESSAGE);
            
        }
        catch(NumberFormatException ex)
        {
                JOptionPane.showMessageDialog(null,"Please Input Digits Only!","Error!",JOptionPane.ERROR_MESSAGE); 
                ex.printStackTrace();
        }
        catch(SQLException nex)
        {
                JOptionPane.showMessageDialog(null,"Error Ocurred in DB!","Error!",JOptionPane.ERROR_MESSAGE);
                nex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteEmpActionPerformed

