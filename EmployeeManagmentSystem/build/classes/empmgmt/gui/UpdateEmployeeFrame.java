    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!txtEmpName.isEnabled())
        {
            JOptionPane.showMessageDialog(null,"Please check EmpID before updating!","Message!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try
        {
            int empid = Integer.parseInt(txtEmpNo.getText().trim());
            String EmpName = txtEmpName.getText();
            Double Empsal = Double.parseDouble(txtEmpSal.getText());
            employee e = new employee(empid,EmpName,Empsal);
            Boolean result = EmployeeDao.updateEmployeeById(e);
            if(result)
                JOptionPane.showMessageDialog(null,"Employee Details Updated successfully!","Message!",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null,"Some Error Ocurred!","Error!",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException nex)
        {
            JOptionPane.showMessageDialog(null,"Please input digits only!","Error!",JOptionPane.ERROR_MESSAGE);
            nex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

