    private void btnCheckIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckIDActionPerformed
        if(validateInputs())
        {
            JOptionPane.showMessageDialog(null,"Please Input Emp ID!","Message!",JOptionPane.INFORMATION_MESSAGE);   
            return;
        }
        try
        {
            int empid = Integer.parseInt(txtEmpNo.getText().trim());
            employee e = EmployeeDao.findEmployeeById(empid);
            if(e == null)
            {
                JOptionPane.showMessageDialog(null,"No Employee Found With The Given ID!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException nex)
        {
            JOptionPane.showMessageDialog(null,"Please input digits only!","Error!",JOptionPane.ERROR_MESSAGE);
            nex.printStackTrace();
        }
        txtEmpName.setEnabled(true);
        txtEmpSal.setEnabled(true);
    }//GEN-LAST:event_btnCheckIDActionPerformed

