    private void btnShowAllRecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowAllRecActionPerformed
        txtShowAllEmp.setText("");
        try
        {
            ArrayList<employee> emp = EmployeeDao.getAllEmployees();
            if(emp.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"No Data Found In DB!","Message!",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            StringBuilder sb = new StringBuilder();
            sb.append("EMPID\tEMPNAME\t   EMPSAL\n\n");
            for(employee e:emp)
            {
                sb.append(e.getEmpno()+"\t"+e.getEname()+"\t   "+e.getEsal()+"\n");
                txtShowAllEmp.setText(sb.toString());
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnShowAllRecActionPerformed

