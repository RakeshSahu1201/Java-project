    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(validateInput()==false){
            JOptionPane.showMessageDialog(null,"Please fill all the fields", "Empty Values!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            EmpPojo employee=new EmpPojo();
            employee.setEmpid(jcEmployeeId.getSelectedItem().toString());
            employee.setEmpname(txtEmpName.getText());
            employee.setJob(jcDepartements.getSelectedItem().toString());
            employee.setSal(Double.parseDouble(txtEmpSalary.getText()));
            System.out.println(employee);
        if(EmpDao.updateEmployee(employee)){
            JOptionPane.showMessageDialog(null,"Employee Details has been updated!","Success",JOptionPane.INFORMATION_MESSAGE);
            clearAll();
        }
        else
                //JOptionPane.showMessageDialog(null,"Unable to Update employee Details! Try again","Error",JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(null,"Please unregister the user \nBefore updating the department.! Try again","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null,"Please Provide Numeric value only! Try Again.","Error",JOptionPane.ERROR_MESSAGE);
            nfe.printStackTrace();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"User name already exist! Try Again.","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jcEmployeeIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmployeeIdActionPerformed
        String item=jcEmployeeId.getSelectedItem().toString();
        System.out.println(item);
        try{
            EmpPojo obj=EmpDao.getEmployeeById(item);
            if (obj == null){
                JOptionPane.showMessageDialog(null,"Error in DB No data found.","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
             
            txtEmpName.setText(obj.getEmpname());
            jcDepartements.setSelectedItem(obj.getJob().toUpperCase());
            txtEmpSalary.setText(String.valueOf(obj.getSal()));
              
             
        }
        catch(SQLException ex){
           ex.printStackTrace();
       }
    }//GEN-LAST:event_jcEmployeeIdActionPerformed

