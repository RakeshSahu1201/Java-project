    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        if (validateInput() == false) {
            JOptionPane.showMessageDialog(null, "Please fill all the fields", "Empty Values!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        if(!isValidMobileNo(txtMno.getText()))
        {
            JOptionPane.showMessageDialog(null, "please input valid mobile number!","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!isValidOTP(txtOTP.getText()) || patient.getOtp() != Integer.parseInt(txtOTP.getText())){
            JOptionPane.showMessageDialog(null, "OTP is not valid!","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        try
        {
            String doctorName = DoctorsDao.getDoctorNameById(patient.getDoctorId());
            AppointmentPojo appointment = new AppointmentPojo();
            appointment.setPatientId(patient.getPatientId());
            appointment.setPatientName(patient.getFirstName()+patient.getLastName());
            appointment.setStatus(patient.getAptStatus());
            appointment.setOpd(patient.getOpd());
            appointment.setAppointmentDate(patient.getDate().toString());
            appointment.setDoctorName(doctorName);
            appointment.setMobileNo(patient.getMno());
            System.out.println("appointment book");
            boolean result= PatientDao.addPatient(patient) && AppointmentDao.addAppointment(appointment);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Appointment book successfully","Success!", JOptionPane.INFORMATION_MESSAGE);
                ViewAllPatientFrame vapf = new ViewAllPatientFrame();
                vapf.setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "somethimg wrong while booking appointment \nplease try again later!", "Failed", JOptionPane.ERROR_MESSAGE);
            }
       }
       catch(SQLException ex)
       {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null, "Can't book Appointment now \n!Try again later", "Exception",  JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btnVerifyActionPerformed

