    public static boolean isValidMobileNo(String str)  
    {  
        //(0/91): number starts with (0/91)  { /* removed */ }
        //[6-9]: starting of the number may contain a digit between 0 to 9  
        //[0-9]: then contains digits 0 to 9  
        Pattern ptrn = Pattern.compile("[6-9][0-9]{9}");  
        //the matcher() method creates a matcher that will match the given input against this pattern  
        Matcher match = ptrn.matcher(str);  
        //returns a boolean value  
        return (match.find() && match.group().equals(str));  
    }
    public static boolean isValidOTP(String str)  
    {  
        //[7-9]: starting of the number may contain a digit between 0 to 9  
        //{4}: then contains upto lenght 4  
        Pattern ptrn = Pattern.compile("[0-9]{4}");  
        //the matcher() method creates a matcher that will match the given input against this pattern  
        Matcher match = ptrn.matcher(str);  
        //returns a boolean value  
        return (match.find() && match.group().equals(str));  
    }
