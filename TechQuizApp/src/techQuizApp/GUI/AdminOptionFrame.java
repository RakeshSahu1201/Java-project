    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        JFrame frame = null;
        if(jrSetPaper.isSelected())
        {
            frame = new SetPaperFrame();
        }
        else if(jrEditPaper.isSelected())
        {
            frame = new EditPaperFrame();
        }
        else if(jrRegisterStudent.isSelected())
        {
            frame = new RegisterStudentFrame();
        }
        else if(jrViewScore.isSelected())
        {
            frame = new ViewScoresFrame();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please make a selection first.", "Notification", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        frame.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnDoTaskActionPerformed

