    private void showDataInTable()
    {
        try
        {
            ArrayList<PerformancePOJO> performanceList = PerformanceDAO.getAStudentScoreCard(UserProfilePOJO.getUserName());
            if(performanceList.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Sorry! No student appeared to this paper yet.","Notification",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            DefaultTableModel tableModel = (DefaultTableModel)jtStudentData.getModel();
            Object [] row = new Object[7];
            for (PerformancePOJO performance: performanceList) {
                row[0] = performance.getUserId();
                row[1] = performance.getExamId();
                row[2] = performance.getRight();
                row[3] = performance.getWrong();
                row[4] = performance.getUnattemped();
                row[5] = performance.getPercentage();
                row[6] = performance.getLanguage();
                tableModel.addRow(row);
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }
