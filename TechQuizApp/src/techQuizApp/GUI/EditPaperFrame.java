    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
        boolean result = validInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(this, "Please Select A Subject.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            ArrayList<String> examIdList = ExamDAO.getExamIdBySubject(subjectName);
            if(examIdList.isEmpty())
            {
                jcSubject.setSelectedIndex(0);
                JOptionPane.showMessageDialog(this, "No Exam Set For The "+subjectName,"Notification",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            jcExamId.removeAllItems();
            for(String examId: examIdList)
            {
                jcExamId.addItem(examId);
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcSubjectActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        boolean result = validInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(this, "Please Select A Subject.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String subject = jcSubject.getSelectedItem().toString();
        String id = jcExamId.getSelectedItem().toString();
        int ans = JOptionPane.showConfirmDialog(this, "Are You Sure You Want To Edit "+id+" Of Subject "+subject,"Comformation",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans == JOptionPane.YES_OPTION)
        {
            try
            {
                int totalQuestion = ExamDAO.getQuestionCountByExam(id);
                ExamPOJO editExam = new ExamPOJO(id, subject, totalQuestion);
                EditQuestionsFrame editQuestion = new EditQuestionsFrame(editExam);
                editQuestion.setVisible(true);
                this.dispose();
            }
            catch(SQLException ex)
            {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

